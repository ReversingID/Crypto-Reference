const uint32_t STREAM_KEY_BYTES     = 32;
const uint32_t STREAM_NONCE_BYTES   = 16;
const uint32_t STREAM_COUNTER_BYTES =  0;

/*
 * stream_port contract
 *
 * Variant : Loiss — 128-bit initial key + 128-bit IV.
 *
 * STREAM_KEY_BYTES     = 32 — key[0..15], 128-bit initial key (IK);
 *                             key[16..31] ignored.
 * STREAM_NONCE_BYTES   = 16 — nonce[0..15], 128-bit IV.
 * STREAM_COUNTER_BYTES =  0 — counter unused.
 *
 * stream_decrypt : same mapping as stream_encrypt.
 */

void
stream_encrypt(
    uint8_t *data, 
    size_t length, 
    const uint8_t *key,
    const uint8_t *nonce, 
    const uint8_t *counter
)
{
    loiss_ctx_t ctx;
    key_setup(&ctx, key, nonce);
    stream_crypt(&ctx, data, length);
}

void
stream_decrypt(
    uint8_t *data, 
    size_t length, 
    const uint8_t *key,
    const uint8_t *nonce, 
    const uint8_t *counter
)
{
    loiss_ctx_t ctx;
    key_setup(&ctx, key, nonce);
    stream_crypt(&ctx, data, length);
}
