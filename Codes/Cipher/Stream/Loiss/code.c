typedef struct loiss_ctx_t {
    uint8_t  s[32];
    uint8_t  y[16];
    uint32_t R;
} loiss_ctx_t;


/* ********************* INTERNAL FUNCTIONS PROTOTYPE ********************* */
static void stream_crypt(loiss_ctx_t *ctx, uint8_t *data, size_t length);
static void key_setup(loiss_ctx_t *ctx, const uint8_t *key, const uint8_t *iv);

static uint8_t  alpha_mul(uint8_t x);
static uint8_t  alpha_inv_mul(uint8_t x);
static uint32_t theta(uint32_t x);
static uint32_t gamma(uint32_t x);
static uint8_t  f_step(loiss_ctx_t *ctx);
static uint8_t  bomm_step(loiss_ctx_t *ctx, uint8_t w);
static void     lfsr_step(loiss_ctx_t *ctx, uint8_t feedback);
static void     clock(loiss_ctx_t *ctx, int init_phase, uint8_t *out_byte);


/* ************************ CRYPTOGRAPHY ALGORITHM ************************ */
static void
stream_crypt(loiss_ctx_t *ctx, uint8_t *data, size_t length)
{
    size_t i;
    uint8_t z;

    for (i = 0; i < length; i++)
    {
        clock(ctx, 0, &z);
        data[i] ^= z;
    }
}

static void
key_setup(loiss_ctx_t *ctx, const uint8_t *key, const uint8_t *iv)
{
    int i;

    for (i = 0; i < 16; i++)
    {
        ctx->s[i] = key[i];
        ctx->s[i + 16] = (uint8_t)(key[i] ^ iv[i]);
        ctx->y[i] = iv[i];
    }

    ctx->R = 0;

    for (i = 0; i < 64; i++)
        clock(ctx, 1, NULL);
}


/* ******************* INTERNAL FUNCTIONS IMPLEMENTATION ******************* */
static uint8_t
alpha_mul(uint8_t x)
{
    if (x & 0x80U)
        return (uint8_t)((x << 1) ^ LOISS_POLY);
    return (uint8_t)(x << 1);
}

static uint8_t
alpha_inv_mul(uint8_t x)
{
    uint8_t bit = (uint8_t)(x & 1U);
    x = (uint8_t)(x >> 1);
    if (bit)
        x ^= (uint8_t)(LOISS_POLY >> 1);
    return x;
}

static uint32_t
theta(uint32_t x)
{
    return x ^ ((x << 2) | (x >> 30)) ^ ((x << 10) | (x >> 22)) ^
           ((x << 18) | (x >> 14)) ^ ((x << 24) | (x >> 8));
}

static uint32_t
gamma(uint32_t x)
{
    return ((uint32_t)LOISS_S1[(x >> 24) & 0xFFU] << 24) |
           ((uint32_t)LOISS_S1[(x >> 16) & 0xFFU] << 16) |
           ((uint32_t)LOISS_S1[(x >>  8) & 0xFFU] <<  8) |
           ((uint32_t)LOISS_S1[ x        & 0xFFU]);
}

static uint8_t
f_step(loiss_ctx_t *ctx)
{
    uint32_t x;
    uint8_t w;

    w = (uint8_t)(ctx->R >> 24);
    x = ((uint32_t)ctx->s[31] << 24) | ((uint32_t)ctx->s[26] << 16) |
        ((uint32_t)ctx->s[20] <<  8) | (uint32_t)ctx->s[7];
    ctx->R = theta(gamma(x ^ ctx->R));
    return w;
}

static uint8_t
bomm_step(loiss_ctx_t *ctx, uint8_t w)
{
    uint8_t h = (uint8_t)(w >> 4);
    uint8_t l = (uint8_t)(w & 0x0FU);
    uint8_t v = (uint8_t)(ctx->y[h] ^ w);
    uint8_t t;

    ctx->y[l] = (uint8_t)(ctx->y[l] ^ LOISS_S2[w]);
    if (h != l)
    {
        ctx->y[h] = (uint8_t)(ctx->y[h] ^ LOISS_S2[ctx->y[l]]);
    }
    else
    {
        t = ctx->y[l];
        ctx->y[h] = (uint8_t)(t ^ LOISS_S2[t]);
    }

    return v;
}

static void
lfsr_step(loiss_ctx_t *ctx, uint8_t feedback)
{
    uint8_t next;
    int i;

    next = (uint8_t)(ctx->s[29] ^ alpha_mul(ctx->s[24]) ^
                     alpha_inv_mul(ctx->s[17]) ^ ctx->s[15] ^
                     ctx->s[11] ^ alpha_mul(ctx->s[5]) ^
                     ctx->s[2] ^ alpha_inv_mul(ctx->s[0]) ^ feedback);

    for (i = 0; i < 31; i++)
        ctx->s[i] = ctx->s[i + 1];
    ctx->s[31] = next;
}

static void
clock(loiss_ctx_t *ctx, int init_phase, uint8_t *out_byte)
{
    uint8_t w;
    uint8_t v;

    w = f_step(ctx);
    v = bomm_step(ctx, w);

    if (!init_phase && out_byte)
        *out_byte = (uint8_t)(ctx->s[0] ^ v);

    lfsr_step(ctx, init_phase ? v : 0);
}


